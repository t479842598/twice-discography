export const messages: Record<LocaleCode, Record<MessageKey, string>> = {
  'zh-CN': zhCN,
  'en-US': enUS,
  'ja-JP': jaJP,
  'ko-KR': koKR,
}

export const supportedLocales = ['zh-CN', 'en-US', 'ja-JP', 'ko-KR'] as const

export const localeLabels: Record<LocaleCode, string> = {
  'zh-CN': '中文',
  'en-US': 'English',
  'ja-JP': '日本語',
  'ko-KR': '한국어',
}

export function isLocaleCode(value: string | null | undefined): value is LocaleCode {
  return supportedLocales.includes(value as LocaleCode)
}

export function translate(locale: LocaleCode, key: MessageKey, params?: Record<string, number | string>) {
  const template = messages[locale]?.[key] || messages['zh-CN'][key] || key
  if (!params) return template

  return Object.entries(params).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template)
}

export function setDocumentLocale(locale: LocaleCode) {
  if (typeof document === 'undefined') return
  document.documentElement.lang = locale
}
